export function normalizeLocale(value?: string | null): Locale | null {
  if (!value) return null;
  const normalized = value.toLowerCase().split(/[-_]/)[0];
  return locales.includes(normalized as Locale) ? (normalized as Locale) : null;
}

export function getLocaleFromAcceptLanguage(header?: string | null): Locale | null {
  if (!header) return null;
  const preferred = header
    .split(",")
    .map((entry) => entry.trim().split(";")[0])
    .find(Boolean);
  return preferred ? normalizeLocale(preferred) : null;
}

export function getLocaleFromPathname(pathname: string): Locale | null {
  const [firstSegment] = pathname.split("/").filter(Boolean);
  return firstSegment ? normalizeLocale(firstSegment) : null;
}

export function getLocalizedPath(pathname: string, locale: Locale): string {
  const normalizedPath = pathname === "/" ? "/" : pathname.replace(/\/+$/, "");
  if (locale === defaultLocale) {
    return normalizedPath === "/" ? "/" : normalizedPath;
  }
  const prefixed = normalizedPath === "/" ? `/${locale}` : `/${locale}${normalizedPath}`;
  return prefixed;
}

export function translate(locale: Locale, key: string, values?: Record<string, string | number>): string {
  const message = translationMessages[locale]?.[key] ?? translationMessages[defaultLocale]?.[key] ?? key;
  if (!values) return message;
  return Object.entries(values).reduce((result, [placeholder, replacement]) => {
    return result.replace(new RegExp(`\\{${placeholder}\\}`, "g"), String(replacement));
  }, message);
}

export function formatNumber(value: number, locale: Locale, options: Intl.NumberFormatOptions = {}): string {
  return new Intl.NumberFormat(locale, options).format(value);
}

export function formatCurrency(value: number, locale: Locale, currency = "USD", options: Intl.NumberFormatOptions = {}): string {
  return new Intl.NumberFormat(locale, {
    style: "currency",
    currency,
    ...options,
  }).format(value);
}

export function formatDate(value: string | number | Date, locale: Locale, options: Intl.DateTimeFormatOptions = {}): string {
  return new Intl.DateTimeFormat(locale, { dateStyle: "medium", ...options }).format(new Date(value));
}

export function formatDateTime(value: string | number | Date, locale: Locale, options: Intl.DateTimeFormatOptions = {}): string {
  return new Intl.DateTimeFormat(locale, { dateStyle: "medium", timeStyle: "short", ...options }).format(new Date(value));
}
